double dfs(double a, double b, double c) {
    double ans = 0;
    if (a==100 or b==100 or c==100) return 0;
    else {
        if (a>0) {ans += (a/(a+b+c))*(1+dfs(a+1,b,c));}
        if (b>0) {ans += (b/(a+b+c))*(1+dfs(a,b+1,c));}
        if (c>0) {ans += (c/(a+b+c))*(1+dfs(a,b,c+1));}
    }
    return ans;
}


